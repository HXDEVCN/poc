package com.spaeth.appbase.adds.xmlview.exception;

public class XmlViewException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public XmlViewException() {
		super();
	}

	public XmlViewException(String message, Throwable cause) {
		super(message, cause);
	}

	public XmlViewException(String message) {
		super(message);
	}

	public XmlViewException(Throwable cause) {
		super(cause);
	}

}
